public class SpclAbility{ //Special Abilities
   //Includes Encounter and Daily ability types
   
   String name;
   String info;
   
   public SpclAbility(){}
   
   //Set Methods
   public void setName(String x){name = x;}
   public void setInfo(String x){info = x;}
   
   //Get Methods
   public String getName(){return name;}
   public String getInfo(){return info;}
   
}